/* Function: NormalizeQuat
 * Purpose:  Normalize the quaternion after updates so the rotation state remains a valid
 *           unit quaternion and does not drift from accumulated numerical error.
 * Returns:  none
 */
/*----------------------------------------------------------------------------------------*/
static void NormalizeQuat(float q[4])
{
  // Compute the quaternion magnitude so it can be scaled back to unit length.
  float norm = sqrtf(q[0]*q[0] + q[1]*q[1] + q[2]*q[2] + q[3]*q[3]);

  // If the norm is large enough to be safe, divide each component by that norm.
  if (norm > 1e-6f) {
    // Normalize the scalar, X, Y, and Z components.
    q[0] /= norm;
    q[1] /= norm;
    q[2] /= norm;
    q[3] /= norm;
  } else {
    // If the quaternion collapses numerically, fall back to the identity rotation.
    q[0] = 1.0f;
    q[1] = 0.0f;
    q[2] = 0.0f;
    q[3] = 0.0f;
  }
}

/*----------------------------------------------------------------------------------------*/
/* Function: IntegrateQuat
 * Purpose:  Apply angular velocity over the current time step to update the object's
 *           orientation quaternion, which controls how the hammer is rendered.
 * Returns:  none
 */
/*----------------------------------------------------------------------------------------*/
static void IntegrateQuat(float q[4], float wx, float wy, float wz, float dt)
{
  float q_dot[4];  // Temporary quaternion derivative used for one integration step.

  q_dot[0] = 0.5f * (-q[1] * wx - q[2] * wy - q[3] * wz); // Change in scalar component.
  q_dot[1] = 0.5f * ( q[0] * wx + q[2] * wz - q[3] * wy); // Change in X vector component.
  q_dot[2] = 0.5f * ( q[0] * wy - q[1] * wz + q[3] * wx); // Change in Y vector component.
  q_dot[3] = 0.5f * ( q[0] * wz + q[1] * wy - q[2] * wx); // Change in Z vector component.

  q[0] += q_dot[0] * dt; // Advance the scalar term by the current frame time.
  q[1] += q_dot[1] * dt; // Advance the X component by the current frame time.
  q[2] += q_dot[2] * dt; // Advance the Y component by the current frame time.
  q[3] += q_dot[3] * dt; // Advance the Z component by the current frame time.
  NormalizeQuat(q);      // Re-normalize so the quaternion stays a valid unit rotation.
}

/*----------------------------------------------------------------------------------------*/
/* Function: ComputeSpinCommands
 * Purpose:  Convert the measured gravity direction and twist rate into x, y, and z spin
 *           commands, including dead zones so small sensor noise does not rotate the object.
 * Returns:  none
 */
/*----------------------------------------------------------------------------------------*/
static void ComputeSpinCommands(const float gravity_body[3], float gz,
                                float *spin_x, float *spin_y, float *spin_z)
{
  // Map X tilt to spin or zero it if under the deadzone.
  *spin_x = fabsf(gravity_body[0]) > TILT_DEADZONE_G ? (gravity_body[0] * TILT_SPIN_GAIN_X) : 0.0f;
  // Map Y tilt to spin and invert direction to match screen motion.
  *spin_y = fabsf(gravity_body[1]) > TILT_DEADZONE_G ? (-gravity_body[1] * TILT_SPIN_GAIN_Y) : 0.0f;
  // Map Z twist rate to yaw spin or ignore tiny twists.
  *spin_z = fabsf(gz) > TWIST_DEADZONE_RAD_S ? (gz * TWIST_SPIN_GAIN) : 0.0f;

  if (fabsf(gravity_body[0]) < LEVEL_STOP_G && fabsf(gravity_body[1]) < LEVEL_STOP_G && fabsf(gz) < LEVEL_STOP_RAD_S) {
    // Force spin to stop when the box is effectively level and not twisting
    *spin_x = 0.0f;
    *spin_y = 0.0f;
    *spin_z = 0.0f;
  }
}

/*----------------------------------------------------------------------------------------*/
/* Function: IsStableForReset
 * Purpose:  Check whether the box is level enough and moving slowly enough to count as
 *           "still", which allows the timed automatic reset to start counting.
 * Returns:  1 if the box is stable enough to count as still, otherwise 0.
 */
/*----------------------------------------------------------------------------------------*/
static uint8_t IsStableForReset(const float gravity_body[3], float motion_mag)
{
  return (fabsf(gravity_body[0]) < STILL_RESET_G) && // Require X tilt to remain within the stillness threshold.
         (fabsf(gravity_body[1]) < STILL_RESET_G) && // Require Y tilt to remain within the stillness threshold.
         (motion_mag < STILL_RESET_RAD_S);           // Require overall angular motion to remain very small.
}

/*----------------------------------------------------------------------------------------*/
/* Function: ResetObjectOrientation
 * Purpose:  Reset the rendered object's quaternion to the identity rotation so the demo
 *           returns to its default orientation after a long still hold.
 * Returns:  none
 */
/*----------------------------------------------------------------------------------------*/
static void ResetObjectOrientation(void)
{
  object_q[0] = 1.0f; // Identity quaternion scalar term.
  object_q[1] = 0.0f; // Identity quaternion X component.
  object_q[2] = 0.0f; // Identity quaternion Y component.
  object_q[3] = 0.0f; // Identity quaternion Z component.
}

/*----------------------------------------------------------------------------------------*/
/* Function: QuaternionToEulerDegrees
 * Purpose:  Convert the EKF quaternion into roll, pitch, and yaw angles in degrees for
 *           the debug overlay shown in the upper-right corner of the screen.
 * Returns:  none
 */
/*----------------------------------------------------------------------------------------*/
static void QuaternionToEulerDegrees(float q0, float q1, float q2, float q3,
                                     float *roll_deg, float *pitch_deg, float *yaw_deg)
{
  float sinr_cosp = 2.0f * (q0 * q1 + q2 * q3);        // Sine/cosine helper term for roll.
  float cosr_cosp = 1.0f - 2.0f * (q1 * q1 + q2 * q2); // Sine/cosine helper term for roll.
  float sinp = 2.0f * (q0 * q2 - q3 * q1);             // Sine helper term for pitch.
  float siny_cosp = 2.0f * (q0 * q3 + q1 * q2);        // Sine/cosine helper term for yaw.
  float cosy_cosp = 1.0f - 2.0f * (q2 * q2 + q3 * q3); // Sine/cosine helper term for yaw.
  float pitch_rad;                                     // Temporary pitch angle in radians.

  *roll_deg = atan2f(sinr_cosp, cosr_cosp) * (180.0f / (float)M_PI); // Convert roll to degrees.

  if (fabsf(sinp) >= 1.0f) {
    pitch_rad = copysignf((float)M_PI / 2.0f, sinp); // Clamp pitch at +/-90 degrees near gimbal lock.
  } else {
    pitch_rad = asinf(sinp); // Normal pitch conversion when inside valid asin range.
  }

  *pitch_deg = pitch_rad * (180.0f / (float)M_PI); // Convert pitch from radians to degrees.
  *yaw_deg = atan2f(siny_cosp, cosy_cosp) * (180.0f / (float)M_PI); // Convert yaw to degrees.
}

/*----------------------------------------------------------------------------------------*/
/* Function: FormatAngleLine
 * Purpose:  Build a short fixed-width text string for one angle line without relying on
 *           floating-point printf support, which is often disabled in embedded builds.
 * Returns:  none
 */
/*----------------------------------------------------------------------------------------*/
static void FormatAngleLine(char label, float degrees, char out[8])
{
  int value = (int)(degrees >= 0.0f ? degrees + 0.5f : degrees - 0.5f); // Round the float angle to the nearest integer.
  int abs_value; // Store the absolute value for digit extraction.

  if (value > 999) value = 999;           // Clamp very large positive values to the display width.
  if (value < -999) value = -999;         // Clamp very large negative values to the display width.
  abs_value = value < 0 ? -value : value; // Remove the sign temporarily so the digits can be split out.

  out[0] = label;                                  // First character is the axis label: R, P, or Y.
  out[1] = ' ';                                    // Insert a space after the label for readability.
  out[2] = (value < 0) ? '-' : ' ';                // Show a minus sign only for negative values.
  out[3] = (char)('0' + ((abs_value / 100) % 10)); // Hundreds digit.
  out[4] = (char)('0' + ((abs_value / 10) % 10));  // Tens digit.
  out[5] = (char)('0' + (abs_value % 10));         // Ones digit
  out[6] = '\0';                                   // Null terminator so GPU_DrawText sees a valid C string.
}

/*----------------------------------------------------------------------------------------*/
/* Function: DrawPoseOverlay
 * Purpose:  Draw the roll, pitch, and yaw values in the top-right corner after the main
 *           scene is rendered so the diagnostic text stays visible on top of the object.
 * Returns:  none
 */
/*----------------------------------------------------------------------------------------*/
static void DrawPoseOverlay(float roll_deg, float pitch_deg, float yaw_deg)
{
  char line[8];  // Temporary string buffer used for each overlay line.

  GPU_FillRect(368, 8, 104, 32, COLOR_BLACK);  // Clear the old overlay.
  FormatAngleLine('R', roll_deg, line);                 // Build the roll text line.
  GPU_DrawText(374, 10, line, COLOR_WHITE, 1); // Draw roll near the top of the overlay box.
  FormatAngleLine('P', pitch_deg, line);                // Build the pitch text line.
  GPU_DrawText(374, 20, line, COLOR_WHITE, 1); // Draw pitch on the middle line.
  FormatAngleLine('Y', yaw_deg, line);                  // Build the yaw text line.
  GPU_DrawText(374, 30, line, COLOR_WHITE, 1); // Draw yaw on the bottom line.
}
