/* Function: main
 * Purpose:  Application entry point. Startup performs hardware initialization,
 *           user-visible calibration screens, and EKF setup. The main loop then reads
 *           sensors, updates the orientation estimate, maps tilt into object motion, and
 *           redraws the scene.
 * Returns:  none
 */
/*----------------------------------------------------------------------------------------*/
int main(void)
{
  // Hardware Setup
  HAL_Init();           // Initialize HAL Layer
  SystemClock_Config(); // Initialize clock
  MX_GPIO_Init();       // Enable GPIO pins
  MX_FMC_Init();        // Initialize LCD pins
  I2C_Bus_Reset();      // Reset I2C bus
  MX_I2C1_Init();       // Initialize I2C pins

  // Application Setup
  Engine3D_Init(); // Initialize 3D renderer for UI and object
  Sensors_Init();  // Verify sensors are connected and configure them

  // Startup and Calibration
  Sensors_ShowStartupCountdown();  // Draw startup UI
  Sensors_Calibrate();             // Startup sensors, calculate sensor bias, and draw Calibration UI
  Sensors_ShowInstructionScreen(); // Draw Instruction UI

  // Extended Kalman Filter (EKF) Setup

  // Get the calibrated gravity and magnetometer reference vectors for the EKF.
  Sensors_GetReferenceFrame(accel_reference_mg, mag_reference);

  EKF_Init(&ekf); // Initialize EKF state

  // Load the startup reference frame so the EKF knows what "level" should look like.
  EKF_SetReference(&ekf,
                   // Accelerometer reference X, mg to g.
                   accel_reference_mg[0] / 1000.0f,
                   // Accelerometer reference Y, mg to g.
                   accel_reference_mg[1] / 1000.0f,
                   // Accelerometer reference Z, mg to g.
                   accel_reference_mg[2] / 1000.0f,
                   // Magnetometer reference X.
                   mag_reference[0],
                   // Magnetometer reference Y.
                   mag_reference[1],
                   // Magnetometer reference Z.
                   mag_reference[2]);

  uint32_t last_tick = HAL_GetTick(); // Current tick count, used for delta time
  float still_time_s = 0.0f;          // Time box has remained still, used for auto-reset

  // Main Infinite Loop
  while (1)
  {
    // Read the latest sensor samples.
    Sensors_Read(acceleration_mg, angular_rate_mdps, mag_val);

    // Convert gyroscope units from milli-degrees per sec (mdps) into radians per second for the EKF.
    float gx = (angular_rate_mdps[0] / 1000.0f) * (M_PI / 180.0f); // X axis
    float gy = (angular_rate_mdps[1] / 1000.0f) * (M_PI / 180.0f); // Y axis
    float gz = (angular_rate_mdps[2] / 1000.0f) * (M_PI / 180.0f); // Z axis

    // Calculate delta time from elapsed tick count
    uint32_t tick = HAL_GetTick(); // Current tick count,
    float dt = (tick > last_tick) ? ((float)(tick - last_tick) / 1000.0f) : 0.01f; // delta time
    last_tick = tick;              // Update last tick to current

    if (dt < 0.005f) dt = 0.005f; // Clamp delta time if too small
    if (dt > 0.03f) dt = 0.03f;   // Clamp delta time if too large

    EKF_Predict(&ekf, gx, gy, gz, dt); // Predict next state from gyro values

    // Convert accelerometer units from mg into g for the EKF update step.
    float ax = acceleration_mg[0] / 1000.0f; // X axis
    float ay = acceleration_mg[1] / 1000.0f; // Y axis
    float az = acceleration_mg[2] / 1000.0f; // Z axis

    if (mag_available) {
        // Magnetometer connected, update EKF with all accel and mag values */
        EKF_Update(&ekf, ax, ay, az, mag_val[0], mag_val[1], mag_val[2]);
    } else {
        // Magnetometer disconnected, update EKF with only accel values
        EKF_Update(&ekf, ax, ay, az, 0, 0, 0);
    }

    // Pull the current quaternion estimate out of the EKF state vector.
    float q0 = ekf.x_hat_data[0];
    float q1 = ekf.x_hat_data[1];
    float q2 = ekf.x_hat_data[2];
    float q3 = ekf.x_hat_data[3];

    // Gravity estimate in body coordinates, derived from the quaternion.
    float gravity_body[3];

    // Angular velocity commands that will be applied to the rendered object.
    float spin_x;
    float spin_y;
    float spin_z;

    // Euler-angle versions of the current EKF orientation for the text overlay.
    float roll_deg;
    float pitch_deg;
    float yaw_deg;

    // Combined angular-motion magnitude used by the stillness detector.
    float motion_mag;

    // If the EKF state becomes invalid, reset it and skip this frame to recover cleanly.
    if (isnan(q0)) { EKF_Init(&ekf); continue; }

    // Estimate gravity in body coordinates from the EKF orientation.
    gravity_body[0] = 2.0f * (q1*q3 - q0*q2);        // X axis
    gravity_body[1] = 2.0f * (q2*q3 + q0*q1);        // Y axis
    gravity_body[2] = q0*q0 - q1*q1 - q2*q2 + q3*q3; // Z axis

    // Turn physical tilt into angular velocity commands for the object.
    ComputeSpinCommands(gravity_body, gz, &spin_x, &spin_y, &spin_z);

    // Integrate those spin commands so the rendered object continues moving smoothly.
    IntegrateQuat(object_q, spin_x, spin_y, spin_z, dt);

    // Auto-reset after a long still hold so the demo can recover itself.
    // Treat the total gyro magnitude as a simple measure of how much the box is rotating.
    motion_mag = sqrtf(gx * gx + gy * gy + gz * gz);

    // Check whether the box is still
    if (IsStableForReset(gravity_body, motion_mag)) {
        still_time_s += dt; // Box is still, increment time

        if (still_time_s >= STILL_RESET_TIME_S) {
            ResetObjectOrientation(); // Box has been idle long enough, reset orientation
            still_time_s = 0.0f;      // Reset timer
        }
    } else {
        still_time_s = 0.0f; // Box is moving, reset timer
    }

    // Compute the display angles shown in the row/pitch/yaw overlay.
    QuaternionToEulerDegrees(q0, q1, q2, q3, &roll_deg, &pitch_deg, &yaw_deg);

    // Render the object from a fixed camera position.
    Engine3D_SetCameraPose(0.0f, 0.0f, CAMERA_BASE_HEIGHT_IN);

    // Pass the updated object quaternion to the renderer.
    Engine3D_SetObjectRotation(object_q[0], object_q[1], object_q[2], object_q[3]);

    // Project the 3D wireframe into 2D line segments for this frame.
    Engine3D_PrepareFrame(); //

    // Draw the scene
    GPU_Render(Engine3D_DrawScene);

    // Draw row/pitch/yaw overlay
    DrawPoseOverlay(roll_deg, pitch_deg, yaw_deg);
  }
}

