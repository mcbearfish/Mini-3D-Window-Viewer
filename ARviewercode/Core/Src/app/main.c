/*----------------------------------------------------------------------------------------*/
#define TILT_SPIN_GAIN_X      4.5f   // Scale factor that turns X-axis tilt into object spin speed.
#define TILT_SPIN_GAIN_Y      4.5f   // Scale factor that turns Y-axis tilt into object spin speed.
#define TWIST_SPIN_GAIN       1.2f   // Scale factor that turns Z-axis twist into yaw spin speed.
#define TILT_DEADZONE_G       0.08f  // Min tilt magnitude in g before tilt starts rotating the object.
#define TWIST_DEADZONE_RAD_S  0.12f  // Min twist rate in rad/s before twist starts rotating the object.
#define LEVEL_STOP_G          0.04f  // Tilt threshold below which the box is treated as level for stopping motion.
#define LEVEL_STOP_RAD_S      0.08f  // Twist-rate threshold below which the box is treated as not twisting.
#define STILL_RESET_G         0.05f  // Max tilt magnitude allowed while counting toward auto-reset.
#define STILL_RESET_RAD_S     0.09f  // Max rotation magnitude allowed while counting toward auto-reset.
#define STILL_RESET_TIME_S    10.0f  // Time in seconds the box must remain still before the orientation resets.
#define CAMERA_BASE_HEIGHT_IN 3.8f   // Camera height above the object in inches.


